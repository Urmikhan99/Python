a="urmi is good girl"
print(len(a))
for i in range(len(a)):
    print(a[i])

'''
len(a) → counts the total number of characters in the string (spaces included).

for i in range(len(a)): → runs a loop from 0 to last index of the string.

a[i] → prints each character one by one.
'''

